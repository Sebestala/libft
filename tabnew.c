#include "libft.h"

char	**tabnew(int x, int y)
{
	int		i;
	char	**tab;

	i = 0;
	tab = ft_memalloc(y + 1);
	while (i <= y)
	{
		tab[i] = ft_memalloc(x + 1);
		i++;
	}
	return (tab);
}
