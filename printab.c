#include "libft.h"

void	printab(char **tab)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (tab[i])
	{
		while (tab[i][j])
		{
			j++;
		}
		write(1, tab[i], j);
		j = 0;
		i++;
	}
}
